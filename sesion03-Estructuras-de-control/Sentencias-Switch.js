//Sentencias Switch
let nota = 5;

switch (nota) {
    case 5: 
        console.log("Excelente");
        break;
    case 4:
        console.log("Buen trabajo");
        break;
    case 3:
        console.log("Deficiente");
        break;
    case 2:
        console.log("Malo");
        break;
    case 1:
        console.log("Pesimo");
        break;

    default: 
        console.log("Error")
        break;
}

